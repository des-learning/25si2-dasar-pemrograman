# list_example.py
# Contoh tipe data LIST (koleksi berurutan, bisa diubah)

buah = ["apel", "pisang", "jeruk"]
print("Daftar buah:", buah)
print("Tipe data:", type(buah))

# Mengakses item dengan index
print("Buah pertama:", buah[0])

# Menambah item
buah.append("mangga")
print("Setelah ditambah:", buah)

# Mengubah item
buah[1] = "anggur"
print("Setelah diubah:", buah)

